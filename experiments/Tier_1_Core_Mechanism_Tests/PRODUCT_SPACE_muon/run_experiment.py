def _safe_sem(values):
    values = np.asarray(values, dtype=float)
    if len(values) <= 1:
        return 0.0
    return float(np.std(values, ddof=1) / np.sqrt(len(values)))



def summarize_method(final_losses, final_kappas):
    final_losses = np.asarray(final_losses, dtype=float)
    final_kappas = np.asarray(final_kappas, dtype=float)
    return {
        "mean_final_loss": float(np.mean(final_losses)),
        "std_final_loss": float(np.std(final_losses)),
        "sem_final_loss": _safe_sem(final_losses),
        "mean_final_kappa": float(np.mean(final_kappas)),
        "std_final_kappa": float(np.std(final_kappas)),
        "sem_final_kappa": _safe_sem(final_kappas),
    }



def sustained_crossover_step(reference_curve, candidate_curve, fraction_threshold=0.8, min_remaining=5):
    """First sustained step where candidate stays above reference for most remaining steps."""
    reference_curve = np.asarray(reference_curve, dtype=float)
    candidate_curve = np.asarray(candidate_curve, dtype=float)
    min_len = min(len(reference_curve), len(candidate_curve))

    for step in range(min_len):
        if candidate_curve[step] > reference_curve[step]:
            remaining = min_len - step
            if remaining > min_remaining:
                frac_above = float(np.mean(candidate_curve[step:min_len] > reference_curve[step:min_len]))
                if frac_above > fraction_threshold:
                    return step
    return None



def compute_crossover_analysis(method_results, seeds):
    """Compare when Muon / Muon+RB overtake SGD in product kappa."""
    muon_steps = []
    muon_rb_steps = []

    for seed_idx, seed in enumerate(seeds):
        sgd_curve = method_results["sgd"]["kappa_curves"][seed_idx]
        muon_curve = method_results["muon"]["kappa_curves"][seed_idx]
        rb_curve = method_results["muon_rebalance"]["kappa_curves"][seed_idx]

        muon_step = sustained_crossover_step(sgd_curve, muon_curve)
        rb_step = sustained_crossover_step(sgd_curve, rb_curve)
        muon_steps.append(muon_step)
        muon_rb_steps.append(rb_step)

    per_seed = []
    for seed, muon_step, rb_step in zip(seeds, muon_steps, muon_rb_steps):
        per_seed.append(
            {
                "seed": int(seed),
                "muon_crossover_step": muon_step,
                "muon_rebalance_crossover_step": rb_step,
            }
        )

    n_no_cross_muon = sum(step is None for step in muon_steps)
    n_no_cross_rb = sum(step is None for step in muon_rb_steps)

    return {
        "per_seed": per_seed,
        "muon_crossover_steps": muon_steps,
        "muon_rebalance_crossover_steps": muon_rb_steps,
        "n_no_cross_muon": int(n_no_cross_muon),
        "n_no_cross_muon_rebalance": int(n_no_cross_rb),
    }



def evaluate_tests(aggregate, crossover, num_seeds):
    """Evaluate the simple benchmark claims tracked by this experiment."""
    mean_final_kappa_sgd = aggregate["sgd"]["mean_final_kappa"]
    mean_final_kappa_muon = aggregate["muon"]["mean_final_kappa"]
    mean_final_kappa_rb = aggregate["muon_rebalance"]["mean_final_kappa"]

    mean_final_loss_sgd = aggregate["sgd"]["mean_final_loss"]
    mean_final_loss_muon = aggregate["muon"]["mean_final_loss"]
    mean_final_loss_rb = aggregate["muon_rebalance"]["mean_final_loss"]

    n_no_cross_muon = crossover["n_no_cross_muon"]
    n_no_cross_rb = crossover["n_no_cross_muon_rebalance"]

    tests = {
        "T1": {
            "description": "Muon+Rebalance mean final kappa < plain Muon mean final kappa",
            "pass": bool(mean_final_kappa_rb < mean_final_kappa_muon),
            "value": float(mean_final_kappa_rb),
            "reference": float(mean_final_kappa_muon),
        },
        "T2": {
            "description": "Muon+Rebalance has strictly more no-crossover seeds than plain Muon",
            "pass": bool(n_no_cross_rb > n_no_cross_muon),
            "value": int(n_no_cross_rb),
            "reference": int(n_no_cross_muon),
        },
        "T3": {
            "description": "Muon+Rebalance mean final loss < plain Muon mean final loss",
            "pass": bool(mean_final_loss_rb < mean_final_loss_muon),
            "value": float(mean_final_loss_rb),
            "reference": float(mean_final_loss_muon),
        },
        "T4": {
            "description": "Muon+Rebalance mean final loss < SGD mean final loss",
            "pass": bool(mean_final_loss_rb < mean_final_loss_sgd),
            "value": float(mean_final_loss_rb),
            "reference": float(mean_final_loss_sgd),
        },
    }

    tests["summary"] = {
        "num_passed": int(sum(test["pass"] for key, test in tests.items() if key.startswith("T"))),
        "num_tests": int(num_seeds * 0 + 4),
    }
    return tests



def build_verdict(aggregate, crossover, tests):
    """Create a calibrated conclusion that separates conditioning from loss."""
    t1 = tests["T1"]["pass"]
    t2 = tests["T2"]["pass"]
    t3 = tests["T3"]["pass"]
    t4 = tests["T4"]["pass"]

    if t1 and t2:
        conditioning_statement = (
            "Periodic product-space rebalancing lowers the average final product "
            "condition number relative to plain Muon and also yields strictly more "
            "seeds with no sustained kappa crossover above SGD."
        )
        headline = "Conditioning control supported, including stronger no-crossover evidence."
    elif t1:
        conditioning_statement = (
            "Periodic product-space rebalancing lowers the average final product "
            "condition number relative to plain Muon, but stronger crossover prevention "
            "is not established by the current no-crossover count."
        )
        headline = "Average conditioning control supported, but stronger crossover prevention is not established."
    else:
        conditioning_statement = (
            "The current run does not establish better end-to-end conditioning control "
            "for Muon+Rebalance relative to plain Muon."
        )
        headline = "Conditioning control not established."

    if t3:
        loss_statement = (
            "On the chosen convergence metric (mean final loss), Muon+Rebalance also "
            "outperforms plain Muon."
        )
    else:
        loss_statement = (
            "On the chosen convergence metric (mean final loss), Muon+Rebalance does "
            "not outperform plain Muon on this run, so loss-improvement claims should "
            "not be made."
        )

    if t4:
        baseline_statement = "Muon+Rebalance still outperforms SGD on mean final loss."
    else:
        baseline_statement = "Muon+Rebalance does not beat SGD on mean final loss in this run."

    return {
        "headline": headline,
        "conditioning_statement": conditioning_statement,
        "loss_statement": loss_statement,
        "baseline_statement": baseline_statement,
        "conditioning_supported": bool(t1),
        "stronger_crossover_supported": bool(t2),
        "loss_improvement_vs_muon_supported": bool(t3),
        "loss_improvement_vs_sgd_supported": bool(t4),
        "num_tests_passed": int(tests["summary"]["num_passed"]),
        "num_tests": int(tests["summary"]["num_tests"]),
        "n_no_cross_muon": int(crossover["n_no_cross_muon"]),
        "n_no_cross_muon_rebalance": int(crossover["n_no_cross_muon_rebalance"]),
    }



