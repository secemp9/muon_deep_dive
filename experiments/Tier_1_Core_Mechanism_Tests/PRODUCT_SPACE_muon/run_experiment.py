"""
Experiment 3.11: Product-Space Muon
===================================

Toy deep-linear benchmark for periodic product-space rebalancing.

This experiment compares three update rules on an L-layer linear network:
  (a) SGD with momentum
  (b) Muon-style per-layer Newton-Schulz gradient orthogonalization
  (c) Muon + periodic end-to-end product-spectrum rebalancing

Benchmark definition (truthful version)
--------------------------------------
A seeded teacher map W_target with condition number 100 is sampled per seed.
Inputs X are drawn from the same per-seed RNG, and targets are generated by

    Y_target = W_target @ X

so the effective learned linear map is the teacher map itself. The implemented
measurements remain intentionally narrow:
  - training loss
  - end-to-end product condition number kappa_prod(W_L ... W_1)

This is therefore a toy conditioning study, not a general claim about Muon.
"""

