# =============================================================================
# LINEAR-NET UTILITIES
# =============================================================================


def matrix_condition_number(matrix, clip=1e15):
    """Condition number via SVD with a conservative floor on tiny singular values."""
    svs = np.linalg.svd(matrix, compute_uv=False)
    if svs[-1] < 1e-15:
        return float(clip)
    return float(svs[0] / svs[-1])



def layer_condition_numbers(weights, clip=1e15):
    """Per-layer condition numbers."""
    return [matrix_condition_number(W, clip=clip) for W in weights]



def init_weights(num_layers, width, rng):
    """Xavier init for square layers."""
    weights = []
    std = np.sqrt(2.0 / (width + width))
    for _ in range(num_layers):
        W = rng.randn(width, width) * std
        weights.append(W.copy())
    return weights



def copy_weights(weights):
    return [W.copy() for W in weights]



def forward_linear(weights, X):
    out = X.copy()
    for W in weights:
        out = W @ out
    return out



def compute_product_matrix(weights):
    """Compute W_L @ ... @ W_1."""
    prod = weights[0].copy()
    for W in weights[1:]:
        prod = W @ prod
    return prod



def product_condition_number(weights):
    """Condition number of the end-to-end product matrix."""
    return matrix_condition_number(compute_product_matrix(weights))



def compute_loss(weights, X, Y_target):
    Y_pred = forward_linear(weights, X)
    diff = Y_pred - Y_target
    return float(0.5 * np.mean(np.sum(diff ** 2, axis=0)))



def compute_gradients(weights, X, Y_target):
    num_layers = len(weights)
    N = X.shape[1]
    activations = [X.copy()]
    for W in weights:
        activations.append(W @ activations[-1])
    delta = (activations[-1] - Y_target) / N
    grads = [None] * num_layers
    for l in range(num_layers - 1, -1, -1):
        grads[l] = delta @ activations[l].T
        if l > 0:
            delta = weights[l].T @ delta
    return grads


# =============================================================================
# NEWTON-SCHULZ ORTHOGONALIZATION
# =============================================================================


def newton_schulz_ortho(M, n_iters):
    norm = np.linalg.norm(M, ord="fro")
    if norm < 1e-12:
        return M
    X = M / norm
    for _ in range(n_iters):
        A = X.T @ X
        X = 1.5 * X - 0.5 * X @ A
    return X


# =============================================================================
# PRODUCT REBALANCE
# =============================================================================


def rebalance_product(weights):
    """
    Compute the end-to-end product W_prod = W_L ... W_1, then redistribute its
    singular values equally across layers while preserving the full product.
    """
    L = len(weights)
    prod = compute_product_matrix(weights)
    U, sigma, Vt = np.linalg.svd(prod, full_matrices=True)

    sigma_balanced = np.power(np.maximum(sigma, 1e-30), 1.0 / L)
    S = np.diag(sigma_balanced)

    new_weights = [S @ Vt]
    for _ in range(1, L - 1):
        new_weights.append(S.copy())
    new_weights.append(U @ S)

    return new_weights


# =============================================================================
# BENCHMARK CONSTRUCTION
# =============================================================================


def make_teacher_problem(width, batch_size, rng, target_condition_number, input_scale):
    """Construct a seeded teacher-map regression problem Y = W_target X."""
    U_t, _ = np.linalg.qr(rng.randn(width, width))
    V_t, _ = np.linalg.qr(rng.randn(width, width))
    sigma_t = np.logspace(np.log10(target_condition_number), 0.0, width)
    W_target = U_t @ np.diag(sigma_t) @ V_t
    X = rng.randn(width, batch_size) * input_scale
    Y_target = W_target @ X

    effective_map = Y_target @ np.linalg.pinv(X)

    return {
        "W_target": W_target,
        "X": X,
        "Y_target": Y_target,
        "nominal_target_kappa": matrix_condition_number(W_target),
        "effective_map_kappa": matrix_condition_number(effective_map),
    }


