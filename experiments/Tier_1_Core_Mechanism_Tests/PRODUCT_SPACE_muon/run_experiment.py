def run_experiment(
    seed=SEED,
    width=WIDTH,
    num_layers=NUM_LAYERS,
    num_steps=NUM_STEPS,
    batch_size=BATCH_SIZE,
    rebalance_every=REBALANCE_EVERY,
    ns_iters=NS_ITERS,
    momentum=MOMENTUM,
    num_seeds=NUM_SEEDS,
    target_condition_number=TARGET_CONDITION_NUMBER,
    input_scale=INPUT_SCALE,
    lr_search_steps=LR_SEARCH_STEPS,
    lr_candidates=LR_CANDIDATES,
    verbose=False,
):
    """Run the full experiment and return structured raw results."""
    t0 = time.perf_counter()

    lr_candidates = [float(lr) for lr in lr_candidates]
    rebalancing_steps = list(range(rebalance_every, num_steps + 1, rebalance_every)) if rebalance_every else []

    method_results = {
        method: {
            "label": METHOD_LABELS[method].replace(f"K={REBALANCE_EVERY}", f"K={rebalance_every}"),
            "final_losses": [],
            "final_kappas": [],
            "loss_curves": [],
            "kappa_curves": [],
        }
        for method in METHOD_ORDER
    }

    seeds = []
    initial_product_kappas = []
    target_condition_numbers = []
    effective_map_condition_numbers = []
    selected_lrs = {method: [] for method in METHOD_ORDER}

    if verbose:
        print("=" * 90)
        print("Experiment 3.11: Product-Space Muon")
        print("=" * 90)
        print(
            f"Setup: {num_layers}-layer deep linear ({width}x{width}), steps={num_steps}, "
            f"rebalance_every={rebalance_every}, seeds={num_seeds}"
        )
        print(
            f"Teacher-map task: target kappa={target_condition_number:.1f}, batch_size={batch_size}, "
            f"input_scale={input_scale}"
        )

    for seed_idx in range(num_seeds):
        run_seed = int(seed + seed_idx * 17)
        rng = np.random.RandomState(run_seed)
        problem = make_teacher_problem(
            width=width,
            batch_size=batch_size,
            rng=rng,
            target_condition_number=target_condition_number,
            input_scale=input_scale,
        )
        X = problem["X"]
        Y_target = problem["Y_target"]

        weights_init = init_weights(num_layers, width, rng)
        init_kappa = product_condition_number(weights_init)

        seeds.append(run_seed)
        initial_product_kappas.append(float(init_kappa))
        target_condition_numbers.append(float(problem["nominal_target_kappa"]))
        effective_map_condition_numbers.append(float(problem["effective_map_kappa"]))

        lr_sgd = find_best_lr(
            weights_init,
            Y_target,
            X,
            method="sgd",
            lr_candidates=lr_candidates,
            lr_search_steps=lr_search_steps,
            num_steps=num_steps,
            momentum=momentum,
            ns_iters=ns_iters,
            rebalance_every=None,
        )
        lr_muon = find_best_lr(
            weights_init,
            Y_target,
            X,
            method="muon",
            lr_candidates=lr_candidates,
            lr_search_steps=lr_search_steps,
            num_steps=num_steps,
            momentum=momentum,
            ns_iters=ns_iters,
            rebalance_every=None,
        )
        lr_muon_rb = find_best_lr(
            weights_init,
            Y_target,
            X,
            method="muon_rebalance",
            lr_candidates=lr_candidates,
            lr_search_steps=lr_search_steps,
            num_steps=num_steps,
            momentum=momentum,
            ns_iters=ns_iters,
            rebalance_every=rebalance_every,
        )

        selected_lrs["sgd"].append(float(lr_sgd))
        selected_lrs["muon"].append(float(lr_muon))
        selected_lrs["muon_rebalance"].append(float(lr_muon_rb))

        if verbose:
            print(f"\n--- Seed {run_seed} ---")
            print(f"Initial product kappa: {init_kappa:.3e}")
            print(
                f"Target/effective map kappa: {problem['nominal_target_kappa']:.3e} / "
                f"{problem['effective_map_kappa']:.3e}"
            )
            print(
                f"Selected LRs -> SGD: {lr_sgd:.4f}, Muon: {lr_muon:.4f}, "
                f"Muon+RB: {lr_muon_rb:.4f}"
            )

        for method, lr, rb_every in (
            ("sgd", lr_sgd, None),
            ("muon", lr_muon, None),
            ("muon_rebalance", lr_muon_rb, rebalance_every),
        ):
            losses, kappas = train(
                copy_weights(weights_init),
                Y_target,
                X,
                num_steps,
                method=method,
                lr=lr,
                momentum=momentum,
                ns_iters=ns_iters,
                rebalance_every=rb_every,
            )
            method_results[method]["final_losses"].append(float(losses[-1]))
            method_results[method]["final_kappas"].append(float(kappas[-1]))
            method_results[method]["loss_curves"].append(losses.tolist())
            method_results[method]["kappa_curves"].append(kappas.tolist())

        if verbose:
            print(
                f"Finals -> SGD loss={method_results['sgd']['final_losses'][-1]:.4e}, "
                f"kappa={method_results['sgd']['final_kappas'][-1]:.3e}"
            )
            print(
                f"          Muon loss={method_results['muon']['final_losses'][-1]:.4e}, "
                f"kappa={method_results['muon']['final_kappas'][-1]:.3e}"
            )
            print(
                f"          Muon+RB loss={method_results['muon_rebalance']['final_losses'][-1]:.4e}, "
                f"kappa={method_results['muon_rebalance']['final_kappas'][-1]:.3e}"
            )

    aggregate = {
        method: summarize_method(
            method_results[method]["final_losses"],
            method_results[method]["final_kappas"],
        )
        for method in METHOD_ORDER
    }
    crossover = compute_crossover_analysis(method_results, seeds)
    tests = evaluate_tests(aggregate, crossover, num_seeds=num_seeds)
    verdict = build_verdict(aggregate, crossover, tests)

    results = {
        "config": {
            "seed": int(seed),
            "width": int(width),
            "num_layers": int(num_layers),
            "num_steps": int(num_steps),
            "batch_size": int(batch_size),
            "rebalance_every": None if rebalance_every is None else int(rebalance_every),
            "rebalancing_steps": rebalancing_steps,
            "ns_iters": int(ns_iters),
            "momentum": float(momentum),
            "num_seeds": int(num_seeds),
            "target_condition_number": float(target_condition_number),
            "input_scale": float(input_scale),
            "lr_search_steps": int(min(lr_search_steps, num_steps)),
            "lr_candidates": lr_candidates,
        },
        "seeds": seeds,
        "initial_product_kappas": initial_product_kappas,
        "target_condition_numbers": target_condition_numbers,
        "effective_map_condition_numbers": effective_map_condition_numbers,
        "selected_lrs": selected_lrs,
        "methods": method_results,
        "aggregate": aggregate,
        "crossover": crossover,
        "tests": tests,
        "verdict": verdict,
        "runtime_seconds": float(time.perf_counter() - t0),
    }

    return results



