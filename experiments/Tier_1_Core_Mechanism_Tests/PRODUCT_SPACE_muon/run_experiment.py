# TRAINING ENGINE
# =============================================================================


def train(
    weights_init,
    Y_target,
    X,
    n_steps,
    method="sgd",
    lr=0.01,
    momentum=MOMENTUM,
    ns_iters=NS_ITERS,
    rebalance_every=None,
):
    """
    Train and return loss and end-to-end product-kappa trajectories.

    method: 'sgd', 'muon', or 'muon_rebalance'
    """
    weights = copy_weights(weights_init)
    velocities = [np.zeros_like(w) for w in weights]

    losses = []
    kappas = []

    for step in range(n_steps):
        loss = compute_loss(weights, X, Y_target)
        kappa = product_condition_number(weights)
        losses.append(loss)
        kappas.append(kappa)

        if np.isnan(loss) or loss > 1e10:
            losses.extend([1e10] * (n_steps - step - 1))
            kappas.extend([1e15] * (n_steps - step - 1))
            break

        grads = compute_gradients(weights, X, Y_target)

        if method == "sgd":
            for i in range(len(weights)):
                velocities[i] = momentum * velocities[i] + grads[i]
                weights[i] = weights[i] - lr * velocities[i]
        elif method in ("muon", "muon_rebalance"):
            for i in range(len(weights)):
                ortho_grad = newton_schulz_ortho(grads[i], n_iters=ns_iters)
                velocities[i] = momentum * velocities[i] + ortho_grad
                weights[i] = weights[i] - lr * velocities[i]

            if method == "muon_rebalance" and rebalance_every is not None:
                if (step + 1) % rebalance_every == 0:
                    weights = rebalance_product(weights)
                    velocities = [np.zeros_like(w) for w in weights]
        else:
            raise ValueError(f"Unknown method: {method}")

    losses.append(compute_loss(weights, X, Y_target))
    kappas.append(product_condition_number(weights))

    return np.array(losses, dtype=float), np.array(kappas, dtype=float)



def find_best_lr(
    weights_init,
    Y_target,
    X,
    method,
    lr_candidates,
    lr_search_steps,
    num_steps,
    momentum,
    ns_iters,
    rebalance_every=None,
):
    """Grid search for the best LR under a short training horizon."""
    best_loss = 1e20
    best_lr = float(lr_candidates[0])
    short_horizon = min(lr_search_steps, num_steps)

    for lr in lr_candidates:
        losses, _ = train(
            copy_weights(weights_init),
            Y_target,
            X,
            short_horizon,
            method=method,
            lr=lr,
            momentum=momentum,
            ns_iters=ns_iters,
            rebalance_every=rebalance_every,
        )
        final_loss = float(losses[-1])
        if not np.isnan(final_loss) and final_loss < best_loss:
            best_loss = final_loss
            best_lr = float(lr)
    return best_lr


