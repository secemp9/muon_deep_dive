#!/usr/bin/env python3
"""
1.3b-ii-A -- Off-policy Hessian eigendirection alignment of SGD vs Muon updates (2x2 net).

This file keeps the original tiny toy probe but narrows the interpretation:
- the training trajectory is generated by SGD only
- Muon is evaluated off-policy as the update it would take at the same SGD checkpoints
- the primary low-curvature diagnostic uses the two smallest-|lambda| Hessian directions
- the legacy "bottom signed eigenvalue" diagnostic is retained as a secondary comparison only

This is a small geometric probe, not a completed mechanistic explanation of the broader
Muon-as-gauge-fixing story.
"""

from __future__ import annotations

import math
import time
from pathlib import Path
from typing import Dict, List, Optional

import numpy as np

SCRIPT_DIR = Path(__file__).resolve().parent
DEFAULT_SEED = 42
DEFAULT_CONFIG = {
    "n_samples": 20,
    "input_dim": 2,
    "hidden_dim": 2,
    "output_dim": 1,
    "epsilon": 1e-4,
    "lr_sgd": 0.01,
    "n_training_steps": 200,
    "measure_steps": [0, 5, 10, 20, 40, 60, 80, 120, 160, 199],
    "top_k": 2,
    "low_curvature_k": 2,
    "legacy_bottom_signed_k": 2,
    "weight_init_scale": 0.5,
}


# ============================================================
# Configuration helpers
# ============================================================
def get_default_config() -> Dict[str, object]:
    """Return a shallow copy of the default experiment configuration."""
    config = dict(DEFAULT_CONFIG)
    config["measure_steps"] = list(DEFAULT_CONFIG["measure_steps"])
    return config


def prepare_config(config: Optional[Dict[str, object]] = None) -> Dict[str, object]:
    """Merge user overrides into the default configuration and derive n_params."""
    prepared = get_default_config()
    if config:
        for key, value in config.items():
            prepared[key] = list(value) if key == "measure_steps" else value
    prepared["measure_steps"] = sorted(int(step) for step in prepared["measure_steps"])
    prepared["n_params"] = int(
        prepared["input_dim"] * prepared["hidden_dim"]
        + prepared["output_dim"] * prepared["hidden_dim"]
    )
    return prepared


# ============================================================
# Helpers
# ============================================================
def pack(W1: np.ndarray, W2: np.ndarray) -> np.ndarray:
    """Flatten W1 (hidden x input) and W2 (output x hidden) into one vector."""
    return np.concatenate([W1.ravel(), W2.ravel()])


def unpack(theta: np.ndarray, config: Dict[str, object]) -> tuple[np.ndarray, np.ndarray]:
    """Reconstruct W1 and W2 from the flattened parameter vector."""
    input_dim = int(config["input_dim"])
    hidden_dim = int(config["hidden_dim"])
    output_dim = int(config["output_dim"])
    w1_size = hidden_dim * input_dim
    W1 = theta[:w1_size].reshape(hidden_dim, input_dim)
    W2 = theta[w1_size:].reshape(output_dim, hidden_dim)
    return W1, W2


def forward(W1: np.ndarray, W2: np.ndarray, X_in: np.ndarray) -> np.ndarray:
    """Deep linear forward pass: y = W2 @ W1 @ x for each sample."""
    hidden = X_in @ W1.T
    out = hidden @ W2.T
    return out


def loss_fn(theta: np.ndarray, X: np.ndarray, Y: np.ndarray, config: Dict[str, object]) -> float:
    """MSE loss given a flattened parameter vector."""
    W1, W2 = unpack(theta, config)
    pred = forward(W1, W2, X)
    return float(np.mean((pred - Y) ** 2))


def compute_gradient(
    theta: np.ndarray,
    X: np.ndarray,
    Y: np.ndarray,
    config: Dict[str, object],
) -> np.ndarray:
    """Gradient of the loss via central finite differences."""
    epsilon = float(config["epsilon"])
    n_params = int(config["n_params"])
    grad = np.zeros(n_params)
    for i in range(n_params):
        e_i = np.zeros(n_params)
        e_i[i] = epsilon
        grad[i] = (
            loss_fn(theta + e_i, X, Y, config) - loss_fn(theta - e_i, X, Y, config)
        ) / (2 * epsilon)
    return grad


def compute_hessian(
    theta: np.ndarray,
    X: np.ndarray,
    Y: np.ndarray,
    config: Dict[str, object],
) -> np.ndarray:
    """Full Hessian via central finite differences."""
    epsilon = float(config["epsilon"])
    n_params = int(config["n_params"])
    H = np.zeros((n_params, n_params))
    for i in range(n_params):
        for j in range(i, n_params):
            e_i = np.zeros(n_params)
            e_j = np.zeros(n_params)
            e_i[i] = epsilon
            e_j[j] = epsilon
            fpp = loss_fn(theta + e_i + e_j, X, Y, config)
            fpm = loss_fn(theta + e_i - e_j, X, Y, config)
            fmp = loss_fn(theta - e_i + e_j, X, Y, config)
            fmm = loss_fn(theta - e_i - e_j, X, Y, config)
            H[i, j] = (fpp - fpm - fmp + fmm) / (4 * epsilon**2)
            H[j, i] = H[i, j]
    return H


def muon_orthogonalize(G: np.ndarray) -> np.ndarray:
    """Return the SVD-based polar factor U @ V^T used here as the Muon direction."""
    U, _, Vt = np.linalg.svd(G, full_matrices=False)
    return U @ Vt


def compute_muon_update_from_gradient(grad: np.ndarray, config: Dict[str, object]) -> np.ndarray:
    """Construct the hypothetical Muon update from a flattened gradient."""
    input_dim = int(config["input_dim"])
    hidden_dim = int(config["hidden_dim"])
    output_dim = int(config["output_dim"])
    w1_size = hidden_dim * input_dim

    grad_W1 = grad[:w1_size].reshape(hidden_dim, input_dim)
    grad_W2 = grad[w1_size:].reshape(output_dim, hidden_dim)

    ortho_W1 = muon_orthogonalize(grad_W1)
    ortho_W2 = muon_orthogonalize(grad_W2)
    return np.concatenate([-ortho_W1.ravel(), -ortho_W2.ravel()])


def project_onto_eigenvectors(update: np.ndarray, eigvecs: np.ndarray) -> np.ndarray:
    """Return |<update_hat, v_i>| for each Hessian eigenvector."""
    norm = np.linalg.norm(update)
    if norm < 1e-12:
        return np.zeros(eigvecs.shape[1])
    update_hat = update / norm
    return np.abs(eigvecs.T @ update_hat)


def get_direction_indices(eigenvalues: np.ndarray, config: Dict[str, object]) -> Dict[str, List[int]]:
    """Identify comparison subsets of eigendirections."""
    top_k = int(config["top_k"])
    low_k = int(config["low_curvature_k"])
    legacy_k = int(config["legacy_bottom_signed_k"])

    top = sorted(np.argsort(eigenvalues)[::-1][:top_k].tolist())
    low_curvature = sorted(np.argsort(np.abs(eigenvalues))[:low_k].tolist())
    legacy_bottom_signed = sorted(np.argsort(eigenvalues)[:legacy_k].tolist())
    return {
        "top": top,
        "low_curvature": low_curvature,
        "legacy_bottom_signed": legacy_bottom_signed,
    }


def squared_projection_mass(projections: np.ndarray, indices: List[int]) -> float:
    """Sum of squared projection magnitudes on a subset of eigendirections."""
    if not indices:
        return 0.0
    return float(np.sum(projections[np.array(indices, dtype=int)] ** 2))


def safe_ratio(numerator: float, denominator: float, eps: float = 1e-15) -> float:
    """Numerically safe ratio helper."""
    return float(numerator / (denominator + eps))


def direction_tag_string(index: int, direction_indices: Dict[str, List[int]]) -> str:
    """Human-readable tags for one eigenvector slot."""
    tags = []
    if index in direction_indices["top"]:
        tags.append("top")
    if index in direction_indices["low_curvature"]:
        tags.append("low|λ|")
    if index in direction_indices["legacy_bottom_signed"]:
        tags.append("legacy-neg")
    return ", ".join(tags) if tags else "mid"


